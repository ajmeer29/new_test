          <p style="color:#000000;">Film editing is often referred to as the “invisible art” because when it is well-practiced, the viewer can become so engaged that he or she is not even aware of the editor’s work. A film editor must creatively work with the layers of images, story, dialogue, music, pacing, as well as the actors’ performances to effectively “re-imagine” and even rewrite the film to craft a cohesive whole. Editors usually play a dynamic role in the making of a film. The temporary elements are usually replaced with more refined final elements by the sound, music, and visual effects teams hired to complete the picture. Editing Software’s are Adobe Premiere, Avid Express Pro, Final Cut Pro.. etc..</p>        
