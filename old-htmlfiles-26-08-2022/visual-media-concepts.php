          <h4>Video Editing</h4>
    <p  style="color:#000000;">Video editing is the process of editing segments of motion video footage, special effects and sound recordings. In theory and the use of non-linear and linear editing systems. Using video or film, a director can communicate non-fictional and fictional events. The goals of editing is to manipulate these events for better or for worse communication. It is a visual art. Film editing is part of the process of filmmaking, which involves selection and combining of shots into sequences and is art of storytelling.</p>
    </div>          
            
