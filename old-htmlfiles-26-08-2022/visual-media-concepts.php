         <div class="half-section-box animate" data-anim-type="fadeInUp" data-anim-delay="500"  > 
          <h4>2D Animation</h4>
    <p>The creation of moving pictures in a two-dimensional environment, such as through “traditional” cel animation or in computerized animation software. 2D animation involves the creation of a high volume of separate drawings that define a sequence. These drawings are then traced with ink onto transparent celluloid sheets called cel, which are scanned and painted using a special application software. These cels are layered on top of each other to create a sequence. The sequence is later edited to synchronise the audio and video content. This technique is widely used in creating characters for animations and cartoon programmes.</p>
    </div>
    
    
    <div class="half-section-box animate" data-anim-type="fadeInUp" data-anim-delay="500"  > 
          <h4>3D Animation</h4>
               <p>The creation of moving pictures in a three-dimensional digital environment. This is done by sequencing consecutive images, or “frames”, that simulate motion by each image showing the next in a gradual progression of steps, filmed by a virtual “camera” and then output to video by a rendering engine. 3D objects are constructed using curves or 2D geometric figures. 3D Software programmes are used to modify the texture, light and color of the object surface. Virtual cameras are used to zoom, focus, illuminate and resize the 3D objects. This technique is commonly used to create animation for television programmes, movies and online and console games.</p>
