 <h3 class="stcode_title7 text-center animate" data-anim-type="fadeInUp" data-anim-delay="300">Editing</h3>
 
    <p class="animate" data-anim-type="fadeInUp" data-anim-delay="400">Editing is the process of selecting and preparing language, images, sound, video, film, Computer Graphics through processes of correction, condensation, organization, and other modifications in various media. A person who edits is called an editor. Editing is a practice that includes creative skills, human relations, and a precise set of methods.</p>
    
